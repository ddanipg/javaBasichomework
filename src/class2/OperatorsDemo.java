package class2;

public class OperatorsDemo {
    public static void main(String[] args) {

        int number1=10;
        int number2=20;
        System.out.println(number1+number2); //addition
        System.out.println(number1-number2);// subtraction
        System.out.println(number1*number2);// multiply
        System.out.println(number1/number2);// Division

        double number3=10;
        double number4=20;
        System.out.println(number3/number4);

        /*
        if you have to store whole numbers always use int
        if you want to store decimal numbers always use double
        is you want to store character you can always ise string
      */


    }
}
