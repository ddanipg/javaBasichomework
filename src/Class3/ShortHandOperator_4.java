package Class3;

public class ShortHandOperator_4 {
    public static void main(String[] args) {
        // we are learning shortcuts to do things.
        int number=10;
        number=number+10; // it is same as writing number+=10
        //shortcut will be number+=10;
        //whatever value is stored in number, variable add 10 to it.
        System.out.println(number);

        int number2=20;
        number2-=10;
        System.out.println(number2);

        int number3=1000;
        number3/=10;
        System.out.println(number3);





    }
}
